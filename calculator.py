#basic calculator

def add(a,b):
    return a+b

def substract(a,b):
    return a-b

def multiply(a,b):
    return a*b  #I'm not mistake..

def divide_new(a,b):
    return a/b

def getMedian(a, b):
    return (a+b)/2

def getRemainder(a, b):
    return a//b

def getAbs(num):
    if num>=0:
        return num
    else:
        return -num
